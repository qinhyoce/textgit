"""
第一节 字符串的格式化输出
·传统格式化输出的方法：%
%d:为数据类型占位
%s:为字符占位
%f:为浮点数占位
·f表达式
"""
s1 = "%s,臣服于我！"%("画笔")
print(s1)
s2 = "只要有芽衣在，就有%d个人的力量了"%(100) # 为整数占位，自动去掉小数
print(s2)
s3 = "只要有芽衣在，就有%f的力量了"%(100) # 为浮点数占位，保留小数
print(s3)
s4 = "只要有芽衣在，就有%.3f的力量了"%(100) # 用.阿拉伯数字确定保留位数
print(s4)
# 2·f表达式
name = "少女"
age = "18"
s5 = f"嗨呀！艾莉西雅永远是{age}的{name}"
print(s5)
s6 = "嗨呀！艾莉西雅永远是{}的{}" .format(age,name)
print(s6)
print("=================分界线=====================")
