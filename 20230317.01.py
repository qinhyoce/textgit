# 2·format的进阶使用
from math import *
print("众所周知，圆周率为{}".format(pi))
print("众所周知，圆周率为{:.2f}".format(pi))# format的四舍五入
print("众所周知，圆周率为{0:.2f}".format(pi,e))
print("众所周知，自然底数为{1:.2f}".format(pi,e))
print("众所周知，自然底数为{1}".format(pi,e))
"""
对于format函数，小括号内的下标同样从0开始
当需要保留小数时可用:.f
当需要保留百分数可用:.%
"""
s7 = "源神中有{:.1%}的男角色"
print(s7.format(7328E-4))
